-(void) refreshTable {
    activityIndicator.hidden = FALSE;
    [activityIndicator startAnimating];
    AppDelegate *appDelegate = (AppDelegate *) [[UIApplication sharedApplication] delegate];
    if (appDelegate.isConnected) {
        [self deleteAllObjects:@"Product"];
        [self initTable];
    } else {
        [self loadFromDisk];
        [self fetchedResultsController];
        
    }
    [activityIndicator stopAnimating];


    
}

-(void) initTable {
    // Create the request.
    AppDelegate *appDelegate = (AppDelegate *) [[UIApplication sharedApplication] delegate];
    NSString *itemsUrl = [appDelegate.url stringByAppendingString:@"rest/items/all"];
    NSURLRequest *request=[NSURLRequest requestWithURL:[NSURL URLWithString:itemsUrl]
                                              cachePolicy:NSURLRequestUseProtocolCachePolicy
                                          timeoutInterval:60.0];
    // create the connection with the request
    // and start loading the data
    parser = [[BookListParser alloc] init];
    parser.managedContext = self.managedObjectContext;
    NSURLConnection *theConnection=[[NSURLConnection alloc] initWithRequest:request delegate:parser];
    NSURLResponse *response = nil;
    NSError *error = nil;
    [NSURLConnection sendSynchronousRequest:request returningResponse:&response error:&error];
    if (theConnection) {
        // Create the NSMutableData to hold the received data.
        // receivedData is an instance variable declared elsewhere.
        bookData = [NSMutableData data];
        [self saveContext];
    } else {
        // Inform the user that the connection failed.
    }
    
}

-(void) loadFromDisk {
    NSManagedObjectContext *moc = [self managedObjectContext];
    NSEntityDescription *entityDescription = [NSEntityDescription
                                              entityForName:@"Product" inManagedObjectContext:moc];
    NSFetchRequest *request = [[NSFetchRequest alloc] init];
    [request setEntity:entityDescription];
    
    
    NSSortDescriptor *sortDescriptor = [[NSSortDescriptor alloc]
                                        initWithKey:@"title" ascending:YES];
    [request setSortDescriptors:@[sortDescriptor]];
    
    NSError *error;
    NSArray *array = [moc executeFetchRequest:request error:&error];
    if (array == nil)
    {
        NSLog(@"Unresolved error %@, %@", error, [error userInfo]);
        abort();
    }
}


